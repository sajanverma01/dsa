function pattern1($n){
    for($i=1;$i<=$n;$i++){
        for($j=1;$j<$i;$j++){
            echo " ";
        }
        for($j=1;$j<=$i;$j++){
            echo "*";
        }
        echo PHP_EOL;
    }
}

function pattern2($n){
    for($i=1;$i<=$n;$i++){
        for($j=1;$j<$n-$i+1;$j++){
            echo " ";
        }
        for($j=1;$j<=$n-$i+1;$j++){
            echo "*";
        }
        echo PHP_EOL;
    }
}

function pattern($n){
    pattern1($n);
    pattern2($n-1);
}


pattern(10);